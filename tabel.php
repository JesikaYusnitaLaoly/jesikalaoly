    <script>
        const table = document.getElementById('perkalian');
        const theadRow = table.querySelector('thead tr');

        // Tambah header kolom 1-10
        for (let i = 1; i <= 10; i++) {
            const th = document.createElement('th');
            th.textContent = i;
            theadRow.appendChild(th);
        }

        // Tambah isi tabel
        const tbody = table.querySelector('tbody');
        for (let i = 1; i <= 10; i++) {
            const row = document.createElement('tr');
            const th = document.createElement('th');
            th.textContent = i;
            row.appendChild(th);

            for (let j = 1; j <= 10; j++) {
                const td = document.createElement('td');
                td.textContent = i * j;
                row.appendChild(td);
            }

            tbody.appendChild(row);
        }
    </script>

